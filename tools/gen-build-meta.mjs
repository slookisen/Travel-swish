import fs from 'fs';
import path from 'path';
import { execSync } from 'child_process';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');

function readJson(p) {
  return JSON.parse(fs.readFileSync(p, 'utf8'));
}

function safeExec(cmd) {
  try {
    return String(execSync(cmd, { cwd: ROOT, stdio: ['ignore', 'pipe', 'ignore'] })).trim();
  } catch {
    return '';
  }
}

const pkg = readJson(path.join(ROOT, 'package.json'));
const sha = safeExec('git rev-parse --short HEAD') || 'nogit';
const builtAt = new Date().toISOString();
const version = `v${pkg.version}+${sha}`;

// Public meta: used at runtime for update detection. Includes builtAt.
const metaPublic = { version, sha, builtAt };

fs.mkdirSync(path.join(ROOT, 'public'), { recursive: true });
fs.writeFileSync(path.join(ROOT, 'public', 'version.json'), JSON.stringify(metaPublic, null, 2) + '\n', 'utf8');

// Build meta: imported by the app. Keep it stable across rebuilds (no timestamps),
// so `npm run build` doesn't dirty the working tree.
const metaBuild = { version, sha };
const buildMetaTs = `// Auto-generated by tools/gen-build-meta.mjs\n\nexport const BUILD_META = ${JSON.stringify(metaBuild, null, 2)} as const;\n`;
fs.writeFileSync(path.join(ROOT, 'src', 'buildMeta.ts'), buildMetaTs, 'utf8');

console.log('ok', metaPublic);
